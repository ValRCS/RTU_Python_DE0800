# let's see how loop values change with different loop types

print("While loop")

i = 0
while i < 5:
    print(i)
    i += 1

print("outside of the loop and i is", i)

print("Done for now")

print("For loop")

for n in range(5):
    print(n)

print("outside of the loop and n is", n)

print("Done for now")